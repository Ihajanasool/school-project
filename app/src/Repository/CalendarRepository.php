class CalendarRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Calendar::class);
    }

    public function add(Calendar $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Calendar $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }
    public function findByEvent(int $koko)
    {
        return $this->createQueryBuilder('s')
            ->addOrderBy('s.start', 'ASC')
            
            ->Where('s.Evenement = :koko')
            
            ->setParameter('koko', $koko)
            
            
            ->getQuery()
            ->getResult();
    }

